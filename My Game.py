def draw_grid():
    for line in range(0, 200):
        pygame.draw.line(screen, (255, 255, 255), (0, line * tile_size), (screen_width, line * tile_size))
        pygame.draw.line(screen, (255, 255, 255), (line * tile_size, 0), (line * tile_size, screen_height))

#####how to move charecter automatically#####
class Player:
    rect: Union[Rect, RectType, None]

    def __init__(self, x, y):
        x = 100
        y = 700-100
        img = pygame.image.load('img/ball.png')
        self.image = pygame.transform.scale(img, (tile_size, tile_size))
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y

    def update(self):
        screen.blit(self.image, self.rect)
        if self.rect.x > 0:
            self.rect.x += 1
        #ทำไม่เป็น
        elif self.rect.x < 500:
            self.rect.x -= 1

class World:
    def __init__(self, data):
        self.tile_list = []

        #load images
        square = pygame.image.load('img/square.jpg')
        triangle_img = pygame.image.load('img/triangle.png')

        row_count = 0
        for row in data:
            col_count = 0
            for tile in row:
                if tile == 1:
                    img = pygame.transform.scale(square, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect)
                    self.tile_list.append(tile)
                if tile == 2:
                    img = pygame.transform.scale(triangle_img, (tile_size, tile_size))
                    img_rect = img.get_rect()
                    img_rect.x = col_count * tile_size
                    img_rect.y = row_count * tile_size
                    tile = (img, img_rect)
                    self.tile_list.append(tile)
                col_count += 1
            row_count += 1

    def draw(self):
        for tile in self.tile_list:
            screen.blit(tile[0], tile[1])


